package basic;

public class OpTest02 {
	public static void main(String[] args) {
		int a = 7;
		int b = 4;
		
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a>=b); //부등호 먼저표시, 띄어서표시 X
		System.out.println(a<=b);
		System.out.println(a==b); //같다 (등호표시2)
		System.out.println(a!=b); //다르다
	}
}
